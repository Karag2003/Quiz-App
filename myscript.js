$(document).ready(function(){
   $("#a1").hide();
   $("#a2").hide();
   $("#a3").hide();
   
   $("#Hide").click(function(){
    $("#a1").hide();
    $("#a2").hide();
    $("#a3").hide();
   });
   
   $("#Show").click(function(){
    $("#a1").show();
    $("#a2").show();
    $("#a3").show();
   });
});
